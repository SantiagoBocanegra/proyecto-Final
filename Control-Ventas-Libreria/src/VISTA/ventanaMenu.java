    public Permisos buscarPermiso(String nombreTabla) {
        Permisos per = new Permisos();
        for (Permisos permiso : permisosRol) {
            if (nombreTabla.equals(permiso.getNombreTabla())) {
                per = permiso;
                break;
            }
        }
        return per;
    }
