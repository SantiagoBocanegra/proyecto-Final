    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        if (permiso != null && permiso.getIdpermisos() != null && permiso.getInsertar()) {
            ventanaCliente ventanaCli = new ventanaCliente(new javax.swing.JDialog(), true);
            ventanaCli.btnMensaje.setEnabled(false);
            ventanaCli.btnEditar.setVisible(false);
            limpiarTabla();
            id = 0;
            entId.setText("Id");
            ventanaCli.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "EL Rol No Tiene Permiso Para Esta Opcion", "Error", 0, null);
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (permiso != null && permiso.getIdpermisos() != null && permiso.getEditar()) {
            if (id > 0) {
                ventanaCliente ventanaCli = new ventanaCliente(new javax.swing.JDialog(), true);
                limpiarTabla();
                MC_Cliente controlCliente = new MC_Cliente();
                Cliente clienteAux = controlCliente.buscarCliente(id);
                id = 0;
                entId.setText("Id");
                ventanaCli.btnMensaje.setEnabled(false);
                ventanaCli.btnGuargar.setVisible(false);
                ventanaCli.entFecha.setEnabled(false);
                ventanaCli.setCliente(clienteAux);
                ventanaCli.mostrarDatosVentana(clienteAux);
                ventanaCli.setVisible(true);
                if (permiso.getVer()) {
                    btnVerTodoActionPerformed(evt);
                }
            } else {
                JOptionPane.showMessageDialog(this, "No Ha Seleccionado Nada", "Informacion", 1, null);
            }
        } else {
            JOptionPane.showMessageDialog(this, "EL Rol No Tiene Permiso Para Esta Opcion", "Error", 0, null);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String parametro = entParametro.getText();
        Date fechaI = entFi.getDate();
        Date feachaF = entFf.getDate();
        List<Cliente> clientes = new ArrayList<>();
        if (parametro != null && !parametro.isEmpty() || rbRangoFecha.isSelected()) {
            if (rbId.isSelected()) {
                MC_Cliente controlCliente = new MC_Cliente();
                int idC = Integer.parseInt(parametro);
                Cliente cli = controlCliente.buscarCliente(idC);
                if (cli != null && cli.getId() != null) {
                    clientes.add(cli);
                }
            }
            if (rbCedula.isSelected()) {
                MC_Cliente controlCliente = new MC_Cliente();
                clientes = controlCliente.buscarClienteCedula(parametro);
            }
            if (rbNombre.isSelected()) {
                MC_Cliente controlCliente = new MC_Cliente();
                clientes = controlCliente.buscarClienteNombre(parametro);
            }
            if (rbApellido.isSelected()) {
                MC_Cliente controlCliente = new MC_Cliente();
                clientes = controlCliente.buscarClienteApellido(parametro);
            }
            if (rbRangoFecha.isSelected()) {
                MC_Cliente controlCliente = new MC_Cliente();
                clientes = controlCliente.buscarClienteRangoFecha(fechaI, feachaF);
            }
            mostrarClientes(clientes);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

