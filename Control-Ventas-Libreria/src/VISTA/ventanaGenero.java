    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        genero = new Genero();
        obtenerElementos();

        if (JOptionPane.showConfirmDialog(this, "Guardar Genero", "Escudo", 1, 3, null) == 0) {
            MC_Genero control = new MC_Genero();
            if (control.nuevoGenero(genero)) {
                JOptionPane.showMessageDialog(this, "Genero Guardado", "Informacion", 1, null);
                this.setVisible(false);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

