    public void obtenerElementos() {
        genero.setNombre(entNombre.getText());
        genero.setDescripcion(entDescripcion.getText());
        genero.setFechacreacion(entFechaCrecion.getDate());
    }

    public void mostrarElementos(Genero genero) {
        entId.setText(String.valueOf(genero.getId()));
        String nombre = "Sin Nombre";
        if (genero.getNombre() != null && !genero.getNombre().isEmpty()) {
            nombre = genero.getNombre();
        }
        String descripcion = "Sin Descripcion";
        if (genero.getDescripcion() != null && !genero.getDescripcion().isEmpty()) {
            descripcion = genero.getDescripcion();
        }
        entNombre.setText(nombre);
        entDescripcion.setText(descripcion);
        if (genero.getFechacreacion() != null) {
            entFechaCrecion.setDate(genero.getFechacreacion());
        }
    }

    public Genero getGenero() {
        return genero;
    }

    public void setGenero(Genero genero) {
        this.genero = genero;
    }

