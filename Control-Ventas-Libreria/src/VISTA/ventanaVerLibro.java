    public void limpiarTabla() {
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(modelo.getRowCount() - 1);
        }
    }
    private void tablaLibroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaLibroMouseClicked
        isbn = Integer.parseInt(modelo.getValueAt(tablaLibro.getSelectedRow(), 1).toString());
        entId.setText(String.valueOf(isbn));
    }//GEN-LAST:event_tablaLibroMouseClicked

    private void btnVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerActionPerformed
        if (isbn > 0) {
            ventanaLibro ventanaLibro = new ventanaLibro(new javax.swing.JDialog(), true);
            MC_Libro controlLibro = new MC_Libro();
            Libro libroAux = controlLibro.buscarLibro(isbn);
            ventanaLibro.btnGuardar.setVisible(false);
            ventanaLibro.btnEditar.setVisible(false);
            ventanaLibro.setLibro(libroAux);
            ventanaLibro.mostrarElementos(libroAux);
            ventanaLibro.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "No  Ha Seleccionado Nada", "Informacion", 1, null);
        }
    }//GEN-LAST:event_btnVerActionPerformed

    private void btnVerTodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerTodoActionPerformed
        limpiarTabla();
        isbn = 0;
        entId.setText("Isbn");
        List<Libro> libros;
        MC_Libro controlLibro = new MC_Libro();
        libros = controlLibro.buscarTodosLibros();
        if (!libros.isEmpty()) {
            for (Libro libro : libros) {
                int id = libro.getIsbn();
                ImageIcon Icon = new ImageIcon(getClass().getResource("/IMAGENES/fotoEmpleado.png"));
                ImageIcon portada = new ImageIcon(Icon.getImage().getScaledInstance(98, 98, Image.SCALE_DEFAULT));
                if (libro.getPortada() != null && libro.getPortada().length > 0) {
                    portada = new ImageIcon(funciones.byte_jpg(libro.getPortada()).getScaledInstance(98, 98, Image.SCALE_DEFAULT));
                }
                String titulo = "Sin Titulo";
                if (libro.getTitulo() != null && !libro.getTitulo().isEmpty()) {
                    titulo = libro.getTitulo();
                }
                String autor = "Sin Autor";
                if (libro.getAutor() != null && !libro.getAutor().isEmpty()) {
                    autor = libro.getAutor();
                }
                String inventario = "Sin Cantidad";
                if (libro.getEstadolibro() != null && !libro.getEstadolibro().isEmpty()) {
                    inventario = libro.getEstadolibro();
                }
                String precio = "0";
                if (libro.getPrecio() != null && libro.getPrecio() > 0) {
                    precio = String.valueOf(libro.getPrecio());
                }
                modelo.addRow(new Object[]{new JLabel(portada), id, titulo, autor, inventario, precio});
            }
        } else {
            JOptionPane.showMessageDialog(this, "No Se Encontraron Libros", "Informacion", 1, null);
        }
    }//GEN-LAST:event_btnVerTodoActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (permiso != null && permiso.getIdpermisos() != null && permiso.getEditar()) {
            if (isbn > 0) {
                ventanaLibro ventanaLibro = new ventanaLibro(new javax.swing.JDialog(), true);
                limpiarTabla();
                MC_Libro controlLibro = new MC_Libro();
                Libro libroAux = controlLibro.buscarLibro(isbn);
                isbn = 0;
                entId.setText("Isbn");
                ventanaLibro.entIsbn.setEditable(false);
                ventanaLibro.btnGuardar.setVisible(false);
                ventanaLibro.setLibro(libroAux);
                ventanaLibro.mostrarElementos(libroAux);
                ventanaLibro.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "No  Ha Seleccionado Nada", "Informacion", 1, null);
            }
        } else {
            JOptionPane.showMessageDialog(this, "EL Rol No Tiene Permiso Para Esta Opcion", "Error", 0, null);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        if (permiso != null && permiso.getIdpermisos() != null && permiso.getInsertar()) {
            ventanaLibro ventanaLibro = new ventanaLibro(new javax.swing.JDialog(), true);
            ventanaLibro.btnEditar.setVisible(false);
            limpiarTabla();
            isbn = 0;
            entId.setText("Isbn");
            ventanaLibro.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "EL Rol No Tiene Permiso Para Esta Opcion", "Error", 0, null);
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

