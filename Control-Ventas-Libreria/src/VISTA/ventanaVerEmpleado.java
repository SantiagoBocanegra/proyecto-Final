    public void limpiarTabla() {
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(modelo.getRowCount() - 1);
        }
    }

    private void tablaEmpleadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaEmpleadoMouseClicked
        id = Integer.parseInt(modelo.getValueAt(tablaEmpleado.getSelectedRow(), 0).toString());
        entId.setText(String.valueOf(id));
    }//GEN-LAST:event_tablaEmpleadoMouseClicked

    private void btnVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerActionPerformed
        if (id > 0) {
            ventanaEmpleado ventanaEmp = new ventanaEmpleado(new javax.swing.JDialog(), true);
            MC_Empleado controlEmpleado = new MC_Empleado();
            Empleado empleadoAux = controlEmpleado.buscarEmpleado(id);
            ventanaEmp.btnGuardar.setVisible(false);
            ventanaEmp.btnEditar.setVisible(false);
            ventanaEmp.entFechaContrato.setEnabled(false);
            ventanaEmp.setEmpleado(empleadoAux);
            ventanaEmp.mostrarElementos(empleadoAux);
            ventanaEmp.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "No Ha Seleccionado Nada", "Informacion", 1, null);
        }
    }//GEN-LAST:event_btnVerActionPerformed

    private void btnVerTodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerTodoActionPerformed
        limpiarTabla();
        id = 0;
        entId.setText("Id");
        SimpleDateFormat formato = new SimpleDateFormat("MM/dd/yyy");
        List<Empleado> empleados;
        MC_Empleado controlEmpleado = new MC_Empleado();
        empleados = controlEmpleado.buscarTodosEmpleados();
        if (!empleados.isEmpty()) {
            for (Empleado empleado : empleados) {
                int id = empleado.getId();
                String cargo = "Sin Cargo";
                if (empleado.getCargo() != null && !empleado.getCargo().isEmpty()) {
                    cargo = empleado.getCargo();
                }
                String primerNombre = "Sin Nombre";
                if (empleado.getNombre() != null && !empleado.getNombre().isEmpty()) {
                    primerNombre = empleado.getNombre();
                }
                String ApellidoMaterno = "Sin Apellido";
                if (empleado.getApellidoMaterno() != null && !empleado.getApellidoMaterno().isEmpty()) {
                    ApellidoMaterno = empleado.getApellidoMaterno();
                }
                String cedula = empleado.getCedula();
                String telefono = "Sin Telefono";
                if (empleado.getTelefono() != null && !empleado.getTelefono().isEmpty()) {
                    telefono = empleado.getTelefono();
                }
                String fecha = "Sin Fecha";
                if (empleado.getFechaContrato() != null) {
                    fecha = formato.format(empleado.getFechaContrato());
                }
                modelo.addRow(new Object[]{id, cargo, primerNombre, ApellidoMaterno, cedula, telefono, fecha});
            }
        } else {
            JOptionPane.showMessageDialog(this, "No Se Encontraron Empleados", "Informacion", 1, null);
        }
    }//GEN-LAST:event_btnVerTodoActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (permiso != null && permiso.getIdpermisos() != null && permiso.getEditar()) {
            if (id > 0) {
                ventanaEmpleado ventanaEmpleado = new ventanaEmpleado(new javax.swing.JDialog(), true);
                limpiarTabla();
                MC_Empleado controlEmpleado = new MC_Empleado();
                Empleado empleadoAux = controlEmpleado.buscarEmpleado(id);
                id = 0;
                entId.setText("Id");
                ventanaEmpleado.btnGuardar.setVisible(false);
                ventanaEmpleado.entFechaContrato.setEnabled(false);
                ventanaEmpleado.setEmpleado(empleadoAux);
                ventanaEmpleado.mostrarElementos(empleadoAux);
                ventanaEmpleado.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "No Ha Seleccionado Nada", "Informacion", 1, null);
            }
        } else {
            JOptionPane.showMessageDialog(this, "EL Rol No Tiene Permiso Para Esta Opcion", "Error", 0, null);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        if (permiso != null && permiso.getIdpermisos() != null && permiso.getInsertar()) {
            ventanaEmpleado ventanaEmp = new ventanaEmpleado(new javax.swing.JDialog(), true);
            ventanaEmp.btnEditar.setVisible(false);
            limpiarTabla();
            id = 0;
            entId.setText("Id");
            ventanaEmp.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "EL Rol No Tiene Permiso Para Esta Opcion", "Error", 0, null);
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

